    private void btnCambiarNombreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambiarNombreActionPerformed
        try {
            // Se declara variable y se inicializa con los datos de la variable archivo tipo File
            // y se concatena con el campo de texto donde se ingresara el nuevo nombre y se delcara el tipo de archivo
            String nuevo = documento.getParent()+ "\\" + txtCambiarNombre.getText() + ".txt";
            // Se crea el nuevo archivo con el nombre que se coloco en el campo para cambiarlo
            File nuevoNombre = new File(nuevo);
            // con ayuda de un boolean y el metodo renameTo se pasa el nuevo nombre del archivo
            boolean cambiarNombre = documento.renameTo(nuevoNombre);
            // Se vailda si se cambio el nombre
            if (cambiarNombre==true){
            // si se cambio el nombre se muestra mensaje al usuario
            JOptionPane.showMessageDialog(null,"Se cambio el nombre del archivo con éxito");
            }else{
            // en caso de existir un error se informa al usuario tambien
            JOptionPane.showMessageDialog(null,"Existe un problema al cambiar el nombre del archivo");
        }
            // llamamos al metodo para limpiar los campos
            limpiar();
        // se declara la excepcion en caso de error 
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Existe un error con el archivo " + ex);
        }
        
    }//GEN-LAST:event_btnCambiarNombreActionPerformed

