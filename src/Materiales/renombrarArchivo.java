    // evento usado en el boton para seleccionar el archivo
    private void btnElegirArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElegirArchivoActionPerformed
        // se declara el tray catch para las excepciones
        try {
            // con FileChooser seleccionaremos el arhcivo a cambiar el nombre creando un objeto
            JFileChooser elegirArchivo = new JFileChooser();
            // se abre la ventana para buscar el archivo en el explorador
            elegirArchivo.showOpenDialog(null);
            // se selecciona y guarda el archivo seleccionado
            documento = elegirArchivo.getSelectedFile();
            // mostramos la ruta en el text field
            txtRutaArchivo.setText(documento.getParent() + "\\" + documento.getName());   
        // se declara la excepcion en caso de error 
        }catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Existe un error con el archivo " + ex);
        }
    }//GEN-LAST:event_btnElegirArchivoActionPerformed

